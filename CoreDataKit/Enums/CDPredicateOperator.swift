public enum CDPredicateOperator: String {
    case equals = "=="
    case greaterThan = ">"
    case greaterThanOrEqual = ">="
    case lessThan = "<"
    case lessThanOrEqual = "<="
    case like = "LIKE"
    case contains = "CONTAINS"
}
